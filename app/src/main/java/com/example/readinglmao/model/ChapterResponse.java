package com.example.readinglmao.model;

public class ChapterResponse {
    private int id;
    private int mangaId;
    private String name;
    private boolean status;

    public int getId() {
        return id;
    }

    public int getMangaId() {
        return mangaId;
    }

    public String getName() {
        return name;
    }

    public boolean isStatus() {
        return status;
    }
}

